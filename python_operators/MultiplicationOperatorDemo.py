#Multiplication Operator Learning

a,b = 10,12
print("Multiplication of two integer")
print("a =",a, "b =",b, "a*b =", a*b)

#Example to multiply integer and float numbers
a,b =10,20.5
print("Multiplication of integer & float values")
print("a = {}, b = {}, a*b = {}".format(a,b,a*b))

#Example to multiply of two flat numbers
a,b = -5.55, 6.75E-3
print("Multiplication of integer & float values")
print("a = {}, b = {}, a*b = {}".format(a,b,a*b))

a=10+5j
