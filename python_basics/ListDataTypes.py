#List data types learning

list = ['test', 786, 2.301, "Kunja", 7.53j]
tinylist = [132, 'small']

print (list)                #Prints complete list
print (list[0])             #prints 1st element of the list
print (list[1:3])           #prints elements starts from 2nd till 3rd
print (list[2:])            #prints elements starts from 3rd
print (tinylist * 2)        #prints elements two times
print (list + tinylist)     #prints concatenated list 