#String variable declaration
str = "Welcome to python learning"
print(type(str))

stri = "Hello World"

print (stri)                #print complete string
print (stri[0])             #print 1st character of the string
print (stri[2:5])           #print characters starting from 3rd to 5th
print (stri[2:])            #print characters starting from 3rd character
print (stri * 2)            #Print String two times 
print (stri + " TEST ")     #prints concatenated string 
